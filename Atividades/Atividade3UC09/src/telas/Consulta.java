    public void MostrarTela(TelaInicial obj, int posPaciente) {
        this.telaInicial = obj;
        this.PosPaciente = posPaciente;
        this.paciente = ListaPacientes.getPaciente(posPaciente);
        setVisible(true);
        carregarFormulario();
        btnFinalizar.addActionListener(this);

    }

    private void carregarFormulario() {
        jcbConsultaRealizada.setSelected(false);
        btnFinalizar.setEnabled(false);

        txtReceitaObs.addKeyListener(this);

        if (this.paciente.getConsultaRealizada().equals("Sim")) {
            jcbConsultaRealizada.setSelected(true);
            jcbConsultaRealizada.setEnabled(false);
            btnFinalizar.setEnabled(false);
            txtReceitaObs.setEnabled(false);
        }

        txtReceitaObs.setText(this.paciente.getReceitaObs());
    }

    private void acaoBotao() {

        this.paciente.setReceitaObs(txtReceitaObs.getText());

        ListaPacientes.Atualizar(paciente, PosPaciente);

        telaInicial.atualizarTabela();

        dispose();
    }

