    @Override
    public void keyReleased(KeyEvent e) {
        if (e.getSource() == txtReceitaObs) {
            btnFinalizar.setEnabled(true);
        }
    }

    @Override
    public void actionPerformed(ActionEvent e) {
        if (e.getSource() == btnFinalizar) {
            jcbConsultaRealizada.setSelected(true);
            
            if (jcbConsultaRealizada.isSelected()) {
                this.paciente.setConsultaRealizada("Sim");
            }
        

        }
    }

